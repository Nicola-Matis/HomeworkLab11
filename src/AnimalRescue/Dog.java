package AnimalRescue;

public class Dog {
    String name;
    int age;
    short healthBar;
    short hungerBar;
    short mood;
    String favoriteFood;
    String favoriteActivity;

    public Dog (String dogName,int dogAge,short dogHealthBar,short dogHungerBar,short dogMood,String dogFavoriteFood,String dogFavoriteActivity){
        name = dogName;
        age = dogAge;
        healthBar = dogHealthBar;
        hungerBar = dogHungerBar;
        mood = dogMood;
        favoriteFood = dogFavoriteFood;
        favoriteActivity = dogFavoriteActivity;
    }
}

